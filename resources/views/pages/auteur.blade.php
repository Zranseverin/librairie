      <h3>Biographie</h3>
      <p>Frank Patrick Herbert est un auteur américain de science-fiction, principalement connu pour son roman <em>Dune</em> (1965) et ses cinq suites. Né à Tacoma (Washington), il travailla comme journaliste avant de se consacrer à l'écriture.</p>
      <p>Son œuvre maîtresse, <em>Dune</em>, est l'un des romans de science-fiction les plus vendus de tous les temps. Explorant des thèmes comme l'écologie, la religion, la politique et la condition humaine, Herbert a créé un univers d'une richesse inégalée qui continue d'inspirer auteurs et cinéastes.</p>
      <p>Il reçut le Prix Nebula en 1965 et le Prix Hugo en 1966 pour <em>Dune</em>. Son fils Brian Herbert, en collaboration avec Kevin J. Anderson, a prolongé l'univers avec de nombreux préquels et suites posthumes.</p>
